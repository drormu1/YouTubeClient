const translate = (term) => {
    const regEx = /^[a-zA-Z,.;:\-'"* ]*$/;
  
    var heb="";
    
      if(regEx.test(term))
      {    
        for(var i=0; i< term.length; i++)    
        {       
          var k = term[i];                
          heb += (dicHeb[k] == null ? ' ' : dicHeb[k]);                              
        }       
        return heb;     
      }
      return term;
  }

